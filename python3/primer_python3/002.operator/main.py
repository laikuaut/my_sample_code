# coding: utf-8

def main():
    value1 = 4
    value2 = 6

    print('{} + {} = {}'.format(value1, value2, value1 + value2))
    print('{} - {} = {}'.format(value1, value2, value1 - value2))
    print('{} * {} = {}'.format(value1, value2, value1 * value2))
    print('{} / {} = {}'.format(value1, value2, value1 / value2))
    print('{} % {} = {}'.format(value1, value2, value1 % value2))
    print('{} ** {} = {}'.format(value1, value2, value1 ** value2))

if __name__ == "__main__":
    main()
